<h3>Getting Started</h3>
<ol>
	<li>Complete required fields in Settings tab, and review the rest</li>
	<li>Review Link Designer tab</li>
	<li>Visit a few of your website pages/posts that have more than 250 words</li>
	<li>Reload same pages/posts to see new amazon links</li>
	<li>Refine target products at Amazon per keyword in the Keywords tab</li>
</ol>

