<h3>Getting Started</h3>
<ol>
	<li>Complete required fields in "Settings" tab</li>
	<li>Visit a page or post and return to the plug-in admin</li>
	<li>Click or refresh the "Keywords" tab in the uContext plugin to see if keyword appear in left column</li>
	<li>Refine target products from the catalog per keyword in the "Settings" tab and/or in individual keywords</li>
</ol>

